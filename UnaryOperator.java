
public class UnaryOperator {
	public static void main(String args[]){  
		int x=10;  
		System.out.println(x++);//10 first prints incremented value stores after this step
		System.out.println(++x);//12  first increments the stored value and prints 
		System.out.println(x--);//12 
		System.out.println(--x);//10  
	}

}
